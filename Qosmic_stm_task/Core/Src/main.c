/* USER CODE BEGIN 0 */
static void ControlLoop_RunOneCycle(void)
{
  uint16_t dac_x_counts;
  uint16_t dac_y_counts;

  if (HAL_ADC_Start(&hadc1) != HAL_OK)
  {
    FSMControl_SetState(PAT_STATE_FAULT);
    return;
  }

  if (HAL_ADC_PollForConversion(&hadc1, CONTROL_LOOP_ADC_TIMEOUT_TICKS) != HAL_OK)
  {
    (void)HAL_ADC_Stop(&hadc1);
    FSMControl_SetState(PAT_STATE_FAULT);
    return;
  }
  qpd_x_raw = (uint16_t)HAL_ADC_GetValue(&hadc1);

  if (HAL_ADC_PollForConversion(&hadc1, CONTROL_LOOP_ADC_TIMEOUT_TICKS) != HAL_OK)
  {
    (void)HAL_ADC_Stop(&hadc1);
    FSMControl_SetState(PAT_STATE_FAULT);
    return;
  }
  qpd_y_raw = (uint16_t)HAL_ADC_GetValue(&hadc1);

  if (HAL_ADC_Stop(&hadc1) != HAL_OK)
  {
    FSMControl_SetState(PAT_STATE_FAULT);
    return;
  }

  /* No dedicated ADC power channel is configured yet; use full-scale placeholder. */
  power_raw = ADC_POWER_DEFAULT_COUNTS;

  FSMControl_Update(qpd_x_raw, qpd_y_raw, power_raw);

  dac_x_counts = FSMControl_NormToDAC(g_ctrl.dac_x);
  dac_y_counts = FSMControl_NormToDAC(g_ctrl.dac_y);

  /* SPI2 is used as DAC output path: transmit X and Y channel words each cycle. */
  if (HAL_SPI_Transmit(&hspi2, (uint8_t *)&dac_x_counts, 1U,
                       CONTROL_LOOP_SPI_TIMEOUT_TICKS) != HAL_OK)
  {
    FSMControl_SetState(PAT_STATE_FAULT);
    return;
  }
  if (HAL_SPI_Transmit(&hspi2, (uint8_t *)&dac_y_counts, 1U,
                       CONTROL_LOOP_SPI_TIMEOUT_TICKS) != HAL_OK)
  {
    FSMControl_SetState(PAT_STATE_FAULT);
  }
}

